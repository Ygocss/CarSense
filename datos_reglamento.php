                <br><b>A) RESPETAR LAS SEÑALES DE TRANSITO</b><br>
                Según investigaciones realizadas luego de producidos accidentes de transito ya sea urbano, autopistas, rutas, etc., en casi todos los casos alguno de los conductores ha violado una normativa de tránsito.
Las señales de transito son elementos físicos que nos permiten indicar la forma correcta y segura de transitar por las distintas vías, brindándonos información previa sobre obstáculos y trayectorias alternativas ayudando a las reacciones más rápidas y acertadas teniendo en cuenta que son informaciones anticipadas, contribuyendo además al manejo defensivo.<br><br>
            
<b>B) ENCENDER LAS LUCES DURANTE EL DIA</b></b><br>
Las luces encendidas durante el día hacen que los vehículos sean más distinguibles a distancias mayores. Esto hace que podamos distinguir más fácilmente distintos tipos de vehículos en días nublados, lluviosos, con niebla, al atardecer etc. También distinguir vehículos con antelación y poder aplicar en los casos que sean necesarios acciones de manejo defensivas. Permiten también poder identificar en vías simples de doble sentido ver la dirección que llevan los vehículos solamente fijándonos el color de las luces.
Muchos conductores confunden y piensan que cuando se habla de transitar con las luces encendidas nos referimos a las luces de posición, lo cual es erróneo. Debemos utilizar las LUCES BAJAS O DE MEDIO ALCANCE.<br><br>

<b>C) USAR EL CINTURON DE SEGURIDAD</b><br>
La mayoría de las heridas producidas en un accidente de tránsito son del resultado de distintos impactos que recibe el conductor en el habitáculo. Esto se debe a que el vehículo al producirse un impacto produce una desaceleración muy grande pero el cuerpo del conductor sigue con la misma velocidad que llevaba el vehículo antes de impactar.
Estudios realizados arrojan el resultado que con el uso del cinturón de seguridad se reduce en un 80% la posibilidad de heridas graves o muerte. Se recomienda el uso del cinturón de seguridad no solo del conductor, sino de todos los ocupantes del vehículo.<br><br>

<b>D) USAR EL APOYACABEZAS</b><br>
El apoya cabezas NO ES UN ELEMENTO DE CONFORT del vehículo, ES UN ELEMENTO DE SEGURIDAD diseñado para soportar el desplazamiento de la cabeza hacia atrás en un impacto tanto trasero como frontal, evitando gravísimas lesiones cervicales y el desnucamiento de las personas.
La correcta posición del apoya cabezas es que debe haber una línea imaginaria entre la altura de los ojos del conductor y el centro del mismo, es decir en una misma línea.<br><br>

<b>E) SIN ALCOHOL AL CONDUCIR</b><br>
Dentro del contexto de los errores en la conducción el alcohol se vincula en la mayoría de los casos a distracciones, sueño, capacidad de reacción y excesos de velocidad.
En nuestro país se permite para los conductores de autos 0.5gr de alcohol por litro de sangre, siendo más riguroso para motos y ciclomotores 0.2gr de alcohol por litro de sangre y llegando a los conductores de nuestro sector del transporte con 0gr de alcohol en sangre, es decir NO CONSUMIR ALCOHOL.
El alcohol  produce una depresión del sistema nervioso central deteriorando la función psicomotora y la percepción sensorial llevando a modificar el comportamiento del individua haciéndolo tomar mayores riesgo por su sensación de seguridad.<br><br>
            
<b>F) EL MANEJO NOCTURNO</b><br>
La conducción nocturna produce desde ya una limitación en la visibilidad con respecto a la conducción de día, modificando la percepción del entorno y las velocidades de los vehículos que comparten la ruta.
Teniendo en cuenta estás limitaciones debemos de tomar los mayores recaudos en cuanto al manejo defensivo a fin de evitar accidentes.
Recordemos que en la noche se producen encandilamientos y que los mismos duran alrededor de 8 segundos, siendo los primeros 3 segundos de ceguera total con lo que debemos de tener en cuenta este hecho ya que depende la velocidad en que transitemos cobrará mayor relevancia. Se aconseja en estos casos de encandilamiento buscar una línea lateral u otro punto de referencia a fin de no perder la dirección de nuestro vehículo hasta pasado el peligro provocado por la ceguera.<br><br>

<b>G) DISTANCIA DE SEGUIMIENTO PRUDENTE</b><br>
En todo proceso de frenado intervienen dos factores que son: el tiempo de reacción del conductor y el tiempo de frenado del vehículo.<br>
<br>EL TIEMPO DE REACCION:<br> es el que transcurre desde que el conductor ve un obstáculo hasta que lleva su pie derecho arriba del freno. Este tiempo depende del estado de reacción del conductor que puede ser desde 0.7seg para un conductor con todas sus capacidades en perfecto estado hasta 1.8seg un conductor en pésimo estado.
<br>EL TIEMPO DE FRENADO:<br> dependerá de las condiciones del vehículo respecto de los frenos, el estado de las cubiertas, de la calzada entre otros factores.
Por eso los especialistas aconsejan tomar una distancia de entre 3 a 4 segundos con respecto al vehículo que perseguimos. La llamada regla de los 3 o 4 segundos.<br><br>
            
<b>H) CONDUCCION URBANA</b><br>
El tránsito urbano presenta las características más conflictivas para un conductor pues existen mucho más situaciones impredecibles debido a la gran cantidad de vehículos que transitan.
El principal objetivo del manejo defensivo es la prevención, tratando de utilizar la previsión y anticipación a los hechos tratando de estar alerta de todas las contrapartes que intervienen en la circulación tratando siempre de realizar maniobras evasivas.<br><br>            </p>
            
