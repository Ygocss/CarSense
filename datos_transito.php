            <h1 class="titulo">Transito</h1>
            <p class="parrafo"><br><br>

                A lo largo de la nuestra ciudad existen múltiples vías de circulación, sea la forma de 
                transporte que elijas, es importante conocer sus diferencias, como conductor tienes la 
                responsabilidad de conocer los tipos de vialidades a lo largo de la Ciudad de México.<br>

                Toda la información que necesitas sobre los tipos de vialidades a lo largo de nuestra 
                ciudad la puedes encontrar dentro del Reglamento de Tránsito CDMX 2019, pero todos conocemos
                 lo tedioso que puede ser leer grandes cantidades de información y no encontrar lo que buscamos, 
                 por esa razón en Escuela Drive te presentamos todo lo que tienes que saber sobre los tipos de 
                 vialidades y donde encontrarlas. <br><br>

                 <h2>Tipos de vías</h2>
                 De acuerdo al reglamento podemos definir una vía como un espacio físico que está destinado 
                 al tránsito eficiente de peatones y vehículos, las principales vialidades se dividen en 7: 
                 primarias, secundarias, de acceso controlado, de tránsito calmado, peatonal, ciclista y reversible, 
                 a pesar de que muchas puedan parecer similares es importante diferenciarlas, a continuación, 
                 definiremos cada una.<br><br>

                 <h2>Vías Primarias</h2>
                 Estas vías tienen como objetivo facilitar el flujo del tránsito vehicular continuo o 
                 controlado por semáforo, entre distintas zonas de la Ciudad. Algunas vías cuentan con 
                 carriles exclusivos para la circulación de bicicletas o transporte público, por estas 
                 vías puedes transitar a 50 km/h.<br>

                 Entre las vialidades primarias se encuentran: Oceanía, Bordo de Xochiaca, Av. De la Viga, 
                 Canal del Moral, Año de Juárez, Circunvalación, Plutarco Elías Calles, Calzada del Hueso, 
                 Canal Nacional, Acoxpa, Nuevo León y Acueducto, entre otras.<br><br>

                 <h2>Vías Secundarias</h2>
                 Este tipo de vialidad permite el acceso a las colonias y barrios, además, faculta el flujo 
                 del tránsito vehicular no continúo; en su mayoría conectan con vías primarias y sus intersecciones 
                 pueden estar controladas por semáforos, por estas vías puedes transitar a 40 km/h.<br>

                 Son un poco más angostas que las avenidas primarias, y en algunas zonas, está permitido estacionarse. 
                 El flujo de automóviles es mucho menor al resto de las vialidades, ya que no atraviesan grandes
                 extensiones de la Ciudad.<br>

                 Entre las vialidades secundarias se encuentran: Adolfo Prieto, Isabel la Católica y Rodolfo Usigli.<br><br>
                 
                 <h2>Vías de acceso controlado</h2>
                 Son las vías primarias cuyas intersecciones generalmente son a desnivel; cuentan con carriles 
                 centrales y laterales separados por camellones. La incorporación y desincorporación debe realizarse 
                 a través de carriles de aceleración y desaceleración en puntos específicos, por estas vías puedes 
                 transitar a 80 km/h.<br>

                 Entre las vialidades de acceso controlado se encuentran: Circuito Interior, Anillo Periférico, 
                 Viaducto Miguel Alemán, Tlalpan y Carlos Lazo, Insurgentes, Zaragoza, Constituyentes, San Antonio 
                 Abad, Ejército Nacional, y Aquiles Serdán.<br><br>

                 <h2>Vías de tránsito calmado</h2>
                 Es el área delimitada al interior de colonias, barrios, o pueblos, cuyas vías se diseñan 
                 para reducir el volumen y velocidad del tránsito. De forma tal que peatones, ciclistas y 
                 automovilistas circulen de manera segura. En este apartado se encuentran las zonas escolares, y 
                 de hospitales, y también está restringido por topes, líneas amarillas o blancas, por estas vías 
                 puedes transitar a 30 km/h.<br><br>

                 <h2>Vías peatonales</h2>
                 Esta vía es el área destinada al tránsito de peatones, personas con discapacidad y movilidad limitada. 
                 Asimismo, es el espacio para el alojamiento de instalaciones o mobiliario urbano, en la que el acceso 
                 a vehículos está restringido. La Vía peatonal incluye: aceras, rampas, camellones, isletas, plazas, 
                 parques, calles peatonales, andadores, puentes y cruces peatonales.<br><br>

                 <h2>Vías Ciclistas</h2>
                 La vía ciclista es el área destinada al tránsito de bicicletas. Esta zona puede ser parte de la 
                 superficie de rodadura de las vías o tener un trazo independiente. <br>
                 Las vías para circular en bicicleta 
                 son:<br><br>
                 Carril compartido ciclista: Este carril se ubica en la extrema derecha del área de circulación 
                 vehicular, con un ancho adecuado para permitir que ciclistas y automovilistas compartan el espacio 
                 de forma segura.<br><br>

                 Ciclo carril: El área del Ciclo carril está delimitada con marcas en el pavimento, destinadas al 
                 uso exclusivo de ciclistas.<br><br>
                 
                 Ciclovía: La Ciclovía es el carril confinado exclusivamente para la circulación de los ciclistas; 
                 y está físicamente segregada del tránsito vehicular.<br><br>
                 Calle compartida: Finalmente, la calle compartida es la vía destinada a la circulación prioritaria 
                 de bicicletas. Cuenta con dispositivos que regulan el tránsito de todos los vehículos que circulen 
                 en ella, con la finalidad de compartir el espacio vial de forma segura.<br><br>

                 <h2>Vías Reversibles</h2>
                 Las vías reversibles están destinadas exclusivamente al tránsito de automóviles, con la 
                 posibilidad de cambiar el sentido total o parcial de su circulación en horarios previamente 
                 establecidos y comunicados por la SSP.<br>
